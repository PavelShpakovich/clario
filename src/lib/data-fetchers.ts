export async function fetchTheme(themeId: string) {
  const session = await auth();
  if (!session?.user?.id) {
    return null;
  }

  const { data: themes, error } = await supabaseAdmin
    .from('themes')
    .select('*')
    .eq('id', themeId)
    .eq('user_id', session.user.id)
    .limit(1);

  if (error) {
    console.error('fetchTheme: Supabase error', { themeId, error });
    return null;
  }

  if (!themes?.[0]) {
    console.error('fetchTheme: Theme not found', {
      themeId,
      userId: session.user.id,
      resultCount: themes?.length,
    });
    return null;
  }

  console.log('fetchTheme: Success', { themeId, themeName: themes[0].name });
  return themes[0];
}
